public class IncDecTester
{
	public static void main(String[] args)
	{

		int x=0;

		System.out.println(x);
		x++;
		System.out.println(x);

		System.out.println(x++);

		System.out.println(x);

		System.out.println(++x);

		System.out.println(--x);

		System.out.println(x+=2);

		System.out.println(x-=2);

		System.out.println(x);

		System.out.println(x/=2);

		System.out.println(x*=3);

		System.out.println(15/4); // this shows how 15/4 = 3

		System.out.println(15.0/4); // You have to make 1 of the #'s a double

		System.out.println((double)15/4); // also can use double to force decimals

		System.out.println(Math.PI);




/*
this shows the use of x-- and x++
how to add more than 1(+=2) and all forms of division and multiplacation
*/



	}







}